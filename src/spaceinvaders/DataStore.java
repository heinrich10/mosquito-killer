package spaceinvaders;

public class DataStore {
	private int x;
	private int y;
	
	public DataStore(){
		
	}
	
	public void setX(int x){
		this.x = x;
	}
	
	public void setY(int y){
		this.y = y;
	}
	
	public int getY(){
		return y;
	}
	
	public int getX(){
		return x;
	}
}
